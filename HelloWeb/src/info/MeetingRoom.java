package info;

public class MeetingRoom {
	int id;
	int rname;
	int companyid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRname() {
		return rname;
	}
	public void setRname(int rname) {
		this.rname = rname;
	}
	public int getCompanyid() {
		return companyid;
	}
	public void setCompanyid(int companyid) {
		this.companyid = companyid;
	}
	
}
